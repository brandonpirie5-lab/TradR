-- Enable necessary extensions
create extension if not exists "uuid-ossp";

-- Profiles table (linked to auth.users)
create table if not exists profiles (
  id uuid references auth.users on delete cascade primary key,
  username text,
  balance numeric(12,2) default 100.00,   -- starter balance for demo (real money balance)
  created_at timestamptz default now()
);

-- Contests
create table if not exists contests (
  id bigint generated by default as identity primary key,
  title text not null,
  entry_fee numeric(10,2) not null,
  first_prize numeric(10,2) not null,
  total_prizes numeric(10,2) not null,
  max_entries integer,
  status text default 'open' check (status in ('open','active','closed')),
  starting_portfolio numeric(12,2) default 100000,
  assets text[] default '{}',
  ends_at timestamptz,
  created_at timestamptz default now()
);

-- Participations: user's virtual portfolio in a contest
create table if not exists participations (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  contest_id bigint references contests on delete cascade not null,
  cash numeric(12,2) default 100000,           -- virtual cash
  positions jsonb default '[]'::jsonb,          -- e.g. [{"symbol":"NVDA","shares":120,"avgPrice":128.5}]
  starting_value numeric(12,2),
  final_value numeric(12,2),
  final_rank integer,
  payout numeric(12,2) default 0,
  created_at timestamptz default now(),
  unique(user_id, contest_id)
);

-- Trade log (virtual portfolio trades — separate from real-money transactions)
create table if not exists trade_log (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  contest_id bigint references contests on delete cascade not null,
  symbol text not null,
  side text not null check (side in ('buy','sell')),
  shares numeric(12,4) not null,
  price numeric(12,4) not null,
  total numeric(12,2) not null,
  created_at timestamptz default now()
);

-- Transactions (real money side: deposits, entry fees, payouts)
create table if not exists transactions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  type text not null check (type in ('deposit','entry_fee','payout','withdrawal')),
  amount numeric(12,2) not null,
  description text,
  contest_id bigint references contests,
  created_at timestamptz default now()
);

-- ============================================
-- Row Level Security (RLS) - IMPORTANT
-- ============================================

alter table contests enable row level security;
alter table profiles enable row level security;
alter table participations enable row level security;
alter table transactions enable row level security;
alter table trade_log enable row level security;

-- Contests (public read for lobby)
create policy "Public can view contests"
  on contests for select using (true);

-- Profiles
create policy "Authenticated users can view profiles"
  on profiles for select using (auth.uid() is not null);

create policy "Users can update own profile"
  on profiles for update using (auth.uid() = id);

-- Participations (leaderboard requires reading all entries in a contest)
create policy "Authenticated users can view participations"
  on participations for select using (auth.uid() is not null);

create policy "Users can insert own participations"
  on participations for insert with check (auth.uid() = user_id);

create policy "Users can update own participations"
  on participations for update using (auth.uid() = user_id);

-- Transactions
create policy "Users can view own transactions"
  on transactions for select using (auth.uid() = user_id);

create policy "Users can insert own transactions"
  on transactions for insert with check (auth.uid() = user_id);

-- Trade log
create policy "Users can view own trades"
  on trade_log for select using (auth.uid() = user_id);

create policy "Users can insert own trades"
  on trade_log for insert with check (auth.uid() = user_id);

