create or replace function public.join_contest(p_contest_id bigint)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_contest contests%rowtype;
  v_balance numeric;
  v_count integer;
  v_new_balance numeric;
begin
  if v_user_id is null then
    raise exception 'Not authenticated';
  end if;

  select * into v_contest from contests where id = p_contest_id for update;
  if not found then raise exception 'Contest not found'; end if;
  if v_contest.status = 'closed' then raise exception 'Contest is closed'; end if;

  select count(*) into v_count from participations where contest_id = p_contest_id;
  if v_contest.max_entries is not null and v_count >= v_contest.max_entries then
    raise exception 'Contest is full';
  end if;

  if exists (select 1 from participations where user_id = v_user_id and contest_id = p_contest_id) then
    raise exception 'Already joined this contest';
  end if;

  select balance into v_balance from profiles where id = v_user_id for update;
  if v_balance is null then raise exception 'Profile not found'; end if;
  if v_balance < v_contest.entry_fee then raise exception 'Insufficient balance'; end if;

  v_new_balance := v_balance - v_contest.entry_fee;
  update profiles set balance = v_new_balance where id = v_user_id;

  insert into participations (user_id, contest_id, cash, positions, starting_value)
  values (v_user_id, p_contest_id, v_contest.starting_portfolio, '[]'::jsonb, v_contest.starting_portfolio);

  insert into transactions (user_id, type, amount, description, contest_id)
  values (v_user_id, 'entry_fee', -v_contest.entry_fee, 'Entry for ' || v_contest.title, p_contest_id);

  update contests set status = 'active' where id = p_contest_id and status = 'open';

  return jsonb_build_object(
    'success', true,
    'new_balance', v_new_balance,
    'entry_count', v_count + 1
  );
end;
$$;

create or replace function public.execute_trade(
  p_contest_id bigint,
  p_symbol text,
  p_side text,
  p_shares numeric,
  p_price numeric
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_part participations%rowtype;
  v_contest contests%rowtype;
  v_positions jsonb;
  v_cash numeric;
  v_cost numeric;
  v_elem jsonb;
  v_i int;
  v_old_shares numeric;
  v_old_avg numeric;
  v_new_shares numeric;
  v_new_avg numeric;
  v_found boolean := false;
  v_new_positions jsonb := '[]'::jsonb;
begin
  if v_user_id is null then raise exception 'Not authenticated'; end if;
  if p_shares <= 0 then raise exception 'Invalid share amount'; end if;
  if p_side not in ('buy', 'sell') then raise exception 'Invalid trade side'; end if;
  if p_price <= 0 then raise exception 'Invalid price'; end if;

  select * into v_contest from contests where id = p_contest_id;
  if not found then raise exception 'Contest not found'; end if;
  if v_contest.status = 'closed' then raise exception 'Contest is closed'; end if;
  if v_contest.assets is null or not (p_symbol = any(v_contest.assets)) then
    raise exception 'Symbol not allowed in this contest';
  end if;

  select * into v_part from participations
  where user_id = v_user_id and contest_id = p_contest_id for update;
  if not found then raise exception 'You are not in this contest'; end if;

  v_cash := v_part.cash;
  v_positions := coalesce(v_part.positions, '[]'::jsonb);
  v_cost := round(p_shares * p_price, 2);

  if p_side = 'buy' then
    if v_cash < v_cost then raise exception 'Insufficient cash'; end if;
    v_cash := v_cash - v_cost;

    for v_i in 0..jsonb_array_length(v_positions) - 1 loop
      v_elem := v_positions->v_i;
      if v_elem->>'symbol' = p_symbol then
        v_old_shares := (v_elem->>'shares')::numeric;
        v_old_avg := (v_elem->>'avgPrice')::numeric;
        v_new_shares := v_old_shares + p_shares;
        v_new_avg := (v_old_shares * v_old_avg + v_cost) / v_new_shares;
        v_new_positions := v_new_positions || jsonb_build_array(
          jsonb_build_object('symbol', p_symbol, 'shares', v_new_shares, 'avgPrice', v_new_avg)
        );
        v_found := true;
      else
        v_new_positions := v_new_positions || jsonb_build_array(v_elem);
      end if;
    end loop;

    if not v_found then
      v_new_positions := v_new_positions || jsonb_build_array(
        jsonb_build_object('symbol', p_symbol, 'shares', p_shares, 'avgPrice', p_price)
      );
    end if;
  else
    for v_i in 0..jsonb_array_length(v_positions) - 1 loop
      v_elem := v_positions->v_i;
      if v_elem->>'symbol' = p_symbol then
        v_old_shares := (v_elem->>'shares')::numeric;
        if v_old_shares < p_shares then raise exception 'Insufficient shares'; end if;
        v_cash := v_cash + v_cost;
        v_new_shares := v_old_shares - p_shares;
        if v_new_shares > 0.0001 then
          v_new_positions := v_new_positions || jsonb_build_array(
            jsonb_build_object('symbol', p_symbol, 'shares', v_new_shares, 'avgPrice', (v_elem->>'avgPrice')::numeric)
          );
        end if;
        v_found := true;
      else
        v_new_positions := v_new_positions || jsonb_build_array(v_elem);
      end if;
    end loop;
    if not v_found then raise exception 'No position to sell'; end if;
  end if;

  update participations
  set cash = round(v_cash, 2), positions = v_new_positions
  where user_id = v_user_id and contest_id = p_contest_id;

  insert into trade_log (user_id, contest_id, symbol, side, shares, price, total)
  values (v_user_id, p_contest_id, p_symbol, p_side, p_shares, p_price, v_cost);

  return jsonb_build_object(
    'cash', round(v_cash, 2),
    'positions', v_new_positions
  );
end;
$$;

grant execute on function public.join_contest(bigint) to authenticated;
grant execute on function public.execute_trade(bigint, text, text, numeric, numeric) to authenticated;